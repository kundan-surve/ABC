package datatypes;

public class Datatypes_2 {

	public static void main(String[] args) {
		
		int a = 0;
		System.out.println(a);

		boolean b = false;
		System.out.println(b);
		
		String s = null;
		System.out.println(s);
		
		char c = 0;
		System.out.println(c);
		
		double d = 0;
		System.out.println(d);
		
		
		
		
		
	}

}
